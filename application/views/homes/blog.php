            <p class="blog-post-meta">August 31, 2015 by <a href="/homes/about">Shawnte&acute Arrington</a></p>
            <div class="embed-responsive embed-responsive-4by3">
  <iframe width="560" height="315" src="https://www.youtube.com/embed/q6doWsvdcf8" frameborder="0" allowfullscreen></iframe>
