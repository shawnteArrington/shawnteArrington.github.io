            <p>The key is to know that things are going to change, to be okay with that, and to be ready and willing to change with them--in a word, adaptability. I believe that adaptability is the new stability. And I believe that a successful career is a stable one, a guaranteed one, and one in which you are indispensable and in demand. The ability to adapt is essential to any process, to any desire to remain relevant, and to any probability of surviving. I've never seen a flower bloom that didn't lean into the sun. By this, I mean if you are not willing to move with your source of sustenance, you will not flourish. If you cannot evolve as the economy and as your profession evolve, your skill set will one day be obsolete.</p>
            <p>Today, it's vital to your career to remain in permanent beta--to be ever working on developing a better version of yourself. Just as any system intent upon being around for a while, be it the legal system, a biological system, etc., you must constantly bend and stretch so as not to break, i.e. become unemployable. Taking things one step further, by seeking change, as opposed to being subject to it, you are not only giving yourself a greater sense of control over your future, but also placing yourself ahead of the curve. To seek change is to strengthen your adaptability and essentially to be a pioneer in your field. Pioneers are not only indispensable and in demand, but create the changes to which the rest of us must adapt. By choosing to adapt, by choosing to be a pioneer, you are not only choosing to be successful, you will be.</p>
